    private void autonumber(){
        try{
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM datapasien ORDER BY id DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()){
                String NoPasien = r.getString("id").substring(2);
                String BR = "" + (Integer.parseInt(NoPasien) + 1);
                String No1 = "";
                
                if(BR.length()==1) {
                    No1 = "00";
                }else if(BR.length()==2){
                    No1 = "0";
                }else if(BR.length()==3){
                    No1 = "";
                }
                
                txId.setText("BR" + No1 + BR);
                
            }else{
                txId.setText("BR001");
            }
            r.close();
            s.close();
        }catch(Exception e){
            System.out.println("autonumber error");
        }
    }
    
