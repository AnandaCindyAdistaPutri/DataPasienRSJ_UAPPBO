    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String id = txId.getText();
        String nama = txNama.getText();
        String nama_ruangan = (String)cbNama_Ruangan.getSelectedItem();
        String tahun_masuk = txTahun_Masuk.getText();
        
        try{
            Connection c = Koneksi.getKoneksi();
            String sql = "INSERT INTO datapasien VALUES (?, ?, ?, ?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, id);
            p.setString(2, nama);
            p.setString(3, nama_ruangan);
            p.setString(4, tahun_masuk);
            p.executeUpdate();
            p.close();
            JOptionPane.showMessageDialog(null, "Data Tersimpan");
        }catch(SQLException e){
            System.out.println("terjadi kesalahan");  
        }finally{
            loadData();
            autonumber();
            clear();
        }     
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int i = jTable1.getSelectedRow();
        if(i == -1){
            return;
        }
        String id = (String)model.getValueAt(i, 0);
        String nama = txNama.getText();
        String nama_ruangan = (String)cbNama_Ruangan.getSelectedItem();
        String tahun_masuk = txTahun_Masuk.getText();
        
        try{
            Connection c = Koneksi.getKoneksi();
            String sql = "UPDATE datapasien SET nama = ?, nama_ruangan = ?, tahun_masuk = ? WHERE id = ?";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, nama);
            p.setString(2, nama_ruangan);
            p.setString(3, tahun_masuk);
            p.setString(4, id);
            
            p.executeUpdate();
            p.close();
            JOptionPane.showMessageDialog(null, "Data Terubah");
            btnSimpan.setEnabled(true);
            clear();
        }catch(SQLException e){
            System.out.println("Update Error");  
        }finally{
            loadData();
            autonumber();
        }         
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int i = jTable1.getSelectedRow();
        if(i == -1){
            return;
        }
        
        String id = (String)model.getValueAt(i, 0);
        
        int question = JOptionPane.showConfirmDialog(null, " Yakin Data Akan Dihapus?","Konfirmasi", JOptionPane.OK_CANCEL_OPTION, 
        JOptionPane.QUESTION_MESSAGE);
        if(question == JOptionPane.OK_OPTION){
            try{
                Connection c = Koneksi.getKoneksi();
            String sql = "DELETE FROM datapasien WHERE id = ?";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, id);
            p.executeUpdate();
            p.close();
            JOptionPane.showMessageDialog(null, "Data Terhapus");
        }catch(SQLException e){
            System.out.println("terjadi kesalahan");  
        }finally{
            loadData();
            autonumber();
            clear();
        }
    }
    if(question == JOptionPane.CANCEL_OPTION){
    }//GEN-LAST:event_btnHapusActionPerformed
}
